    private void logar() {
        try {

            String login_usuario, senha_usuario;

            UsuarioDTO objusuarioutil = new UsuarioDTO();
            objusuarioutil.setLogin_usuario(txtLoginUsuario.getText());
            objusuarioutil.setSenha_usuario(String.valueOf(txtSenhaUsuario.getPassword()));

            UsuarioDAO objusuariodao = new UsuarioDAO();
            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario(objusuarioutil);

            if (rsusuariodao.next()) {
                // chamar tela que eu quero abrir
                viewFuncionario objviewFuncionario = new viewFuncionario();
                objviewFuncionario.setVisible(true); // mostrar tela

                dispose(); // fechar tela
            } else {
                // enviar mensagem de erro
                JOptionPane.showMessageDialog(null, "Usu√°rio ou Senha Inv√°lida");

            }

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "viewLogin" + erro);
        }

    }

